package org.program;

public class Pattern_1 {
	public static void main(String[] args) {
		int a=5;
		for (int i = 1; i <=5; i++) {
			//System.out.print('*');
			for (int j =1; j <=a; j++) {
				System.out.print('*');
			}
			for (int k = 1; k <=a; k++) {
				System.out.print(" ");
			}
			System.out.println();
		}
		
}
}
